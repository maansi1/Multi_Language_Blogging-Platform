/**
 * Get translation for a specific key in the current language
 * 
 * @param string $key The translation key
 * @param string $lang The language code (optional)
 * @return string The translated text or the key if not found
 */
function __($key, $lang = null) {
    global $translations;
    
    // If language is not specified, use the current language
    if ($lang === null) {
        $lang = getCurrentLanguage();
    }
    
    // Check if the language exists
    if (!isset($translations[$lang])) {
        $lang = 'en'; // Fallback to English
    }
    
    // Check if the key exists in the language
    if (isset($translations[$lang][$key])) {
        return $translations[$lang][$key];
    }
    
    // If the key doesn't exist in the current language, try English
    if ($lang !== 'en' && isset($translations['en'][$key])) {
        return $translations['en'][$key];
    }
    
    // If the key doesn't exist in any language, return the key
    return $key;
} 